# Una tupla es una secuencia de objetos que es constante porque es inmutable, no la cambiaré

tupla1 = (17,18,19,19,18,19)
print(tupla1[4])

# En al caso de las tuplas son inmutales, o sea que no le puedo quitar ni agregar valores, por ejemplo no saldrán lower, upper ni todo eso por lo mismo que es inmutable

lista1 = [17,18,19,19,18,19] # Esta es una lista que sí se puede modificar o cambiar porque es mutable

